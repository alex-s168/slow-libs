
#include "slowlibs/chacha20.h"

static uint8_t const key[] = {
    0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a,
    0x0b, 0x0c, 0x0d, 0x0e, 0x0f, 0x10, 0x11, 0x12, 0x13, 0x14, 0x15,
    0x16, 0x17, 0x18, 0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f,
};

static uint8_t const nonce[] = {
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x4a,
    0x00, 0x00, 0x00, 0x00, 0x10, 0x00, 0x2a, 0x3a,
};

static uint8_t const expected[] = {
    0xAD, 0xA7, 0xC7, 0xE3, 0x56, 0xC3, 0x58, 0xEC, 0x89, 0x85, 0xC0,
    0xEA, 0x33, 0xBD, 0xC2, 0x38, 0x43, 0xE1, 0xE4, 0xAF, 0x79, 0xF1,
    0x21, 0x62, 0xC4, 0xBD, 0xC5, 0x43, 0xF5, 0x51, 0xEF, 0x10,
};

int main(int argc, char** argv)
{
  slowcrypt_chacha20 state;
  uint8_t buf[32];
  int i;

  (void)argc;
  (void)argv;

  slowcrypt_hchacha(&state, key, nonce, buf, 20);

  for (i = 0; i < 32; i++)
    if (buf[i] != expected[i])
      return 1;

  slowcrypt_chacha20_deinit(&state);
  for (i = 0; i < 32; i++)
    buf[i] = 0;

  return 0;
}
