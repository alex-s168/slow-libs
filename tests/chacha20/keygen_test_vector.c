

#include <stdint.h>

#include "slowlibs/chacha20.h"

static uint8_t const KEY[32] = {
    0x80, 0x81, 0x82, 0x83, 0x84, 0x85, 0x86, 0x87, 0x88, 0x89, 0x8a,
    0x8b, 0x8c, 0x8d, 0x8e, 0x8f, 0x90, 0x91, 0x92, 0x93, 0x94, 0x95,
    0x96, 0x97, 0x98, 0x99, 0x9a, 0x9b, 0x9c, 0x9d, 0x9e, 0x9f,
};

static uint8_t const NONCE[12] = {
    0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
};

static uint8_t const EXPECTED[32] = {
    0x8a, 0xd5, 0xa0, 0x8b, 0x90, 0x5f, 0x81, 0xcc, 0x81, 0x50, 0x40,
    0x27, 0x4a, 0xb2, 0x94, 0x71, 0xa8, 0x33, 0xb6, 0x37, 0xe3, 0xfd,
    0x0d, 0xa5, 0x08, 0xdb, 0xb8, 0xe2, 0xfd, 0xd1, 0xa6, 0x46,
};

int main()
{
  static uint8_t actual[32];
  slowcrypt_chacha20_poly1305_key_gen(actual, KEY, NONCE, 12);
  for (int i = 0; i < 32; i++) {
    if (actual[i] != EXPECTED[i]) {
      return 1;
    }
  }
  return 0;
}